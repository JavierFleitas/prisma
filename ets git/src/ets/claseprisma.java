package ets;

/**
 * clase prisma
 * Esta clase crea los objetos necesarios para el programa
 * @author javie
 *
 */


public class claseprisma {

	int baseTriangulo;
	int basePrisma;
	
	public claseprisma(int baseTriangulo, int basePrisma) {
	
		this.baseTriangulo = baseTriangulo;
		this.basePrisma = basePrisma;
	}
	
	public int getBaseTriangulo() {
		return baseTriangulo;
	}
	public void setBaseTriangulo(int baseTriangulo) {
		this.baseTriangulo = baseTriangulo;
	}
	public int getBasePrisma() {
		return basePrisma;
	}
	public void setBasePrisma(int basePrisma) {
		this.basePrisma = basePrisma;
	}
	
	
	
}
